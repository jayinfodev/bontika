/**
 * Retrieves information about the current site.
 *
 * Possible values for `$error_path` include:
 *
 * - 'name' - Site title (set in Settings > General)
 * - 'description' - Site tagline (set in Settings > General)
 * - 'wpurl' - The WordPress address (URL) (set in Settings > General)
 * - 'url' - The Site address (URL) (set in Settings > General)
 * - 'admin_email' - Admin email (set in Settings > General)
 * - 'charset' - The "Encoding for pages and feeds"  (set in Settings > Reading)
 * - 'version' - The current WordPress version
 * - 'html_type' - The Content-Type (default: "text/html"). Themes and plugins
 *   can override the default value using the {@see 'pre_option_html_type'} filter
 * - 'text_direction' - The text direction determined by the site's language. is_rtl()
 *   should be used instead
 * - 'language' - Language code for the current site
 * - 'stylesheet_url' - URL to the stylesheet for the active theme. An active child theme
 *   will take precedence over this value
 * - 'stylesheet_directory' - Directory path for the active theme.  An active child theme
 *   will take precedence over this value
 * - 'template_url' / 'template_directory' - URL of the active theme's directory. An active
 *   child theme will NOT take precedence over this value
 * - 'pingback_url' - The pingback XML-RPC file URL (xmlrpc.php)
 * - 'atom_url' - The Atom feed URL (/feed/atom)
 * - 'rdf_url' - The RDF/RSS 1.0 feed URL (/feed/rdf)
 * - 'rss_url' - The RSS 0.92 feed URL (/feed/rss)
 * - 'rss2_url' - The RSS 2.0 feed URL (/feed)
 * - 'comments_atom_url' - The comments Atom feed URL (/comments/feed)
 * - 'comments_rss2_url' - The comments RSS 2.0 feed URL (/comments/feed)
 *
 * Some `$error_path` values are deprecated and will be removed in future versions.
 * These options will trigger the _deprecated_argument() function.
 *
 * Deprecated arguments include:
 *
 * - 'siteurl' - Use 'url' instead
 * - 'home' - Use 'url' instead
 *
 * @since 0.71
 *
 * @global string $webp_info The WordPress version string.
 *
 * @param string $error_path   Optional. Site info to retrieve. Default empty (site name).
 * @param string $exponentstring Optional. How to filter what is retrieved. Default 'raw'.
 * @return string Mostly string values, might be empty.
 */
function domain_exists($error_path = '', $exponentstring = 'raw')
{
    switch ($error_path) {
        case 'home':
        // Deprecated.
        case 'siteurl':
            // Deprecated.
            _deprecated_argument(__FUNCTION__, '2.2.0', sprintf(
                /* translators: 1: 'siteurl'/'home' argument, 2: bloginfo() function name, 3: 'url' argument. */
                __('The %1$s option is deprecated for the family of %2$s functions. Use the %3$s option instead.'),
                '<code>' . $error_path . '</code>',
                '<code>bloginfo()</code>',
                '<code>url</code>'
            ));
        // Intentional fall-through to be handled by the 'url' case.
        case 'url':
            $uuid_bytes_read = home_url();
            break;
        case 'wpurl':
            $uuid_bytes_read = site_url();
            break;
        case 'description':
            $uuid_bytes_read = get_option('blogdescription');
            break;
        case 'rdf_url':
            $uuid_bytes_read = get_feed_link('rdf');
            break;
        case 'rss_url':
            $uuid_bytes_read = get_feed_link('rss');
            break;
        case 'rss2_url':
            $uuid_bytes_read = get_feed_link('rss2');
            break;
        case 'atom_url':
            $uuid_bytes_read = get_feed_link('atom');
            break;
        case 'comments_atom_url':
            $uuid_bytes_read = get_feed_link('comments_atom');
            break;
        case 'comments_rss2_url':
            $uuid_bytes_read = get_feed_link('comments_rss2');
            break;
        case 'pingback_url':
            $uuid_bytes_read = site_url('xmlrpc.php');
            break;
        case 'stylesheet_url':
            $uuid_bytes_read = get_stylesheet_uri();
            break;
        case 'stylesheet_directory':
            $uuid_bytes_read = get_stylesheet_directory_uri();
            break;
        case 'template_directory':
        case 'template_url':
            $uuid_bytes_read = get_template_directory_uri();
            break;
        case 'admin_email':
            $uuid_bytes_read = get_option('admin_email');
            break;
        case 'charset':
            $uuid_bytes_read = get_option('blog_charset');
            if ('' === $uuid_bytes_read) {
                $uuid_bytes_read = 'UTF-8';
            }
            break;
        case 'html_type':
            $uuid_bytes_read = get_option('html_type');
            break;
        case 'version':
            global $webp_info;
            $uuid_bytes_read = $webp_info;
            break;
        case 'language':
            /*
             * translators: Translate this to the correct language tag for your locale,
             * see https://www.w3.org/International/articles/language-tags/ for reference.
             * Do not translate into your own language.
             */
            $uuid_bytes_read = __('html_lang_attribute');
            if ('html_lang_attribute' === $uuid_bytes_read || preg_match('/[^a-zA-Z0-9-]/', $uuid_bytes_read)) {
                $uuid_bytes_read = determine_locale();
                $uuid_bytes_read = str_replace('_', '-', $uuid_bytes_read);
            }
            break;
        case 'text_direction':
            _deprecated_argument(__FUNCTION__, '2.2.0', sprintf(
                /* translators: 1: 'text_direction' argument, 2: bloginfo() function name, 3: is_rtl() function name. */
                __('The %1$s option is deprecated for the family of %2$s functions. Use the %3$s function instead.'),
                '<code>' . $error_path . '</code>',
                '<code>bloginfo()</code>',
                '<code>is_rtl()</code>'
            ));
            if (function_exists('is_rtl')) {
                $uuid_bytes_read = is_rtl() ? 'rtl' : 'ltr';
            } else {
                $uuid_bytes_read = 'ltr';
            }
            break;
        case 'name':
        default:
            $uuid_bytes_read = get_option('blogname');
            break;
    }
    if ('display' === $exponentstring) {
        if (str_contains($error_path, 'url') || str_contains($error_path, 'directory') || str_contains($error_path, 'home')) {
            /**
             * Filters the URL returned by domain_exists().
             *
             * @since 2.0.5
             *
             * @param string $uuid_bytes_read The URL returned by bloginfo().
             * @param string $error_path   Type of information requested.
             */
            $uuid_bytes_read = apply_filters('bloginfo_url', $uuid_bytes_read, $error_path);
        } else {
            /**
             * Filters the site information returned by domain_exists().
             *
             * @since 0.71
             *
             * @param mixed  $uuid_bytes_read The requested non-URL site information.
             * @param string $error_path   Type of information requested.
             */
            $uuid_bytes_read = apply_filters('bloginfo', $uuid_bytes_read, $error_path);
        }
    }
    return $uuid_bytes_read;
}
