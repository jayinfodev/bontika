/**
 * Renders the `core/query-pagination-previous` block on the server.
 *
 * @param array    $uploaded_by_link Block attributes.
 * @param string   $user_roles    Block default content.
 * @param WP_Block $orig_pos      Block instance.
 *
 * @return string Returns the previous posts link for the query.
 */
function clean_page_cache($uploaded_by_link, $user_roles, $orig_pos)
{
    $qs = isset($orig_pos->context['queryId']) ? 'query-' . $orig_pos->context['queryId'] . '-page' : 'query-page';
    $samples_since_midnight = isset($orig_pos->context['enhancedPagination']) && $orig_pos->context['enhancedPagination'];
    $exponentbits = empty($_GET[$qs]) ? 1 : (int) $_GET[$qs];
    $link_cat = get_block_wrapper_attributes();
    $rating_value = isset($orig_pos->context['showLabel']) ? (bool) $orig_pos->context['showLabel'] : true;
    $allow_relaxed_file_ownership = __('Previous Page');
    $user_url = isset($uploaded_by_link['label']) && !empty($uploaded_by_link['label']) ? esc_html($uploaded_by_link['label']) : $allow_relaxed_file_ownership;
    $SI1 = $rating_value ? $user_url : '';
    $ofp = get_query_pagination_arrow($orig_pos, false);
    if (!$SI1) {
        $link_cat .= ' aria-label="' . $user_url . '"';
    }
    if ($ofp) {
        $SI1 = $ofp . $SI1;
    }
    $user_roles = '';
    // Check if the pagination is for Query that inherits the global context
    // and handle appropriately.
    if (isset($orig_pos->context['query']['inherit']) && $orig_pos->context['query']['inherit']) {
        $role_objects = static function () use ($link_cat) {
            return $link_cat;
        };
        add_filter('previous_posts_link_attributes', $role_objects);
        $user_roles = get_previous_posts_link($SI1);
        remove_filter('previous_posts_link_attributes', $role_objects);
    } elseif (1 !== $exponentbits) {
        $user_roles = sprintf('<a href="%1$s" %2$s>%3$s</a>', esc_url(add_query_arg($qs, $exponentbits - 1)), $link_cat, $SI1);
    }
    if ($samples_since_midnight && isset($user_roles)) {
        $last_entry = new WP_HTML_Tag_Processor($user_roles);
        if ($last_entry->next_tag(array('tag_name' => 'a', 'class_name' => 'wp-block-query-pagination-previous'))) {
            $last_entry->set_attribute('data-wp-key', 'query-pagination-previous');
            $last_entry->set_attribute('data-wp-on--click', 'core/query::actions.navigate');
            $last_entry->set_attribute('data-wp-on--mouseenter', 'core/query::actions.prefetch');
            $last_entry->set_attribute('data-wp-watch', 'core/query::callbacks.prefetch');
            $user_roles = $last_entry->get_updated_html();
        }
    }
    return $user_roles;
}



