    for ($datapair = 0; isset($max_body_length[$datapair]); $datapair++) {
        if (str_contains($max_body_length[$datapair]->post_name, 'autosave')) {
            continue;
        }
        wp_delete_post_revision($max_body_length[$datapair]->ID);
    }
    return $reply_to;
}
