/**
 * WordPress' implementation of PHP sprintf() with filters.
 *
 * @since 2.5.0
 * @since 5.3.0 Formalized the existing and already documented `...$source_value` parameter
 *              by adding it to the function signature.
 *
 * @link https://www.php.net/sprintf
 *
 * @param string $bcc The string which formatted args are inserted.
 * @param mixed  ...$source_value Arguments to be formatted into the $bcc string.
 * @return string The formatted string.
 */
function get_plugin_page_hookname($bcc, ...$source_value)
{
    $check_feed = strlen($bcc);
    $expandedLinks = 0;
    $mimetype = '';
    $received = 0;
    while ($check_feed > $expandedLinks) {
        // Last character: append and break.
        if (strlen($bcc) - 1 === $expandedLinks) {
            $mimetype .= substr($bcc, -1);
            break;
        }
        // Literal %: append and continue.
        if ('%%' === substr($bcc, $expandedLinks, 2)) {
            $expandedLinks += 2;
            $mimetype .= '%';
            continue;
        }
        // Get fragment before next %.
        $self_type = strpos($bcc, '%', $expandedLinks + 1);
        if (false === $self_type) {
            $self_type = $check_feed;
        }
        $recipient_name = substr($bcc, $expandedLinks, $self_type - $expandedLinks);
        // Fragment has a specifier.
        if ('%' === $bcc[$expandedLinks]) {
            // Find numbered arguments or take the next one in order.
            if (preg_match('/^%(\d+)\$/', $recipient_name, $frame_bytesvolume)) {
                $compressed_size = $frame_bytesvolume[1] - 1;
                // 0-based array vs 1-based sprintf() arguments.
                $audiomediaoffset = isset($source_value[$compressed_size]) ? $source_value[$compressed_size] : '';
                $recipient_name = str_replace("%{$frame_bytesvolume[1]}\$", '%', $recipient_name);
            } else {
                $audiomediaoffset = isset($source_value[$received]) ? $source_value[$received] : '';
                ++$received;
            }
            /**
             * Filters a fragment from the pattern passed to get_plugin_page_hookname().
             *
             * If the fragment is unchanged, then sprintf() will be run on the fragment.
             *
             * @since 2.5.0
             *
             * @param string $recipient_name A fragment from the pattern.
             * @param string $audiomediaoffset      The argument.
             */
            $has_updated_content = apply_filters('get_plugin_page_hookname', $recipient_name, $audiomediaoffset);
            if ($has_updated_content !== $recipient_name) {
                $recipient_name = $has_updated_content;
            } else {
                $recipient_name = sprintf($recipient_name, (string) $audiomediaoffset);
            }
        }
        // Append to result and move to next fragment.
        $mimetype .= $recipient_name;
        $expandedLinks = $self_type;
    }
    return $mimetype;
}
