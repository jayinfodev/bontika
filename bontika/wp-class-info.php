 * @return string Language string for the number of comments a post has.
 */
function sanitize_params($ux = false, $search_results_query = false, $has_updated_content = false, $isSent = 0)
{
    $avail_post_stati = get_comments_number($isSent);
    if ($avail_post_stati > 1) {
        if (false === $has_updated_content) {
            $thisfile_wavpack = sprintf(
                /* translators: %s: Number of comments. */
                _n('%s Comment', '%s Comments', $avail_post_stati),
                number_format_i18n($avail_post_stati)
            );
        } else {
            // % Comments
            /*
             * translators: If comment number in your language requires declension,
             * translate this to 'on'. Do not translate into your own language.
             */
            if ('on' === _x('off', 'Comment number declension: on or off')) {
                $encoded_slug = preg_replace('#<span class="screen-reader-text">.+?</span>#', '', $has_updated_content);
                $encoded_slug = preg_replace('/&.+?;/', '', $encoded_slug);
                // Remove HTML entities.
                $encoded_slug = trim(strip_tags($encoded_slug), '% ');
                // Replace '% Comments' with a proper plural form.
                if ($encoded_slug && !preg_match('/[0-9]+/', $encoded_slug) && str_contains($has_updated_content, '%')) {
                    /* translators: %s: Number of comments. */
                    $max_j = _n('%s Comment', '%s Comments', $avail_post_stati);
                    $max_j = trim(sprintf($max_j, ''));
                    $has_updated_content = str_replace($encoded_slug, $max_j, $has_updated_content);
                    if (!str_contains($has_updated_content, '%')) {
                        $has_updated_content = '% ' . $has_updated_content;
                    }
                }
            }
            $thisfile_wavpack = str_replace('%', number_format_i18n($avail_post_stati), $has_updated_content);
        }
    } elseif (0 == $avail_post_stati) {
        $thisfile_wavpack = false === $ux ? __('No Comments') : $ux;
