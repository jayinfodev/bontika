 * Creates a site.
 *
 * This function runs when a user self-registers a new site as well
 * as when a Super Admin creates a new site. Hook to {@see 'wpmu_new_blog'}
 * for events that should affect all new sites.
 *
 * On subdirectory installations, $expression is the same as the main site's
 * domain, and the path is the subdirectory name (eg 'example.com'
 * and '/blog1/'). On subdomain installations, $expression is the new subdomain +
 * root domain (eg 'blog1.example.com'), and $a_post is '/'.
 *
 * @since MU (3.0.0)
 *
 * @param string $expression     The new site's domain.
 * @param string $a_post       The new site's path.
 * @param string $possible_match      The new site's title.
 * @param int    $g_pclzip_version    The user ID of the new site's admin.
 * @param array  $forbidden_params    Optional. Array of key=>value pairs used to set initial site options.
 *                           If valid status keys are included ('public', 'archived', 'mature',
 *                           'spam', 'deleted', or 'lang_id') the given site status(es) will be
 *                           updated. Otherwise, keys and values will be used to set options for
 *                           the new site. Default empty array.
 * @param int    $expired Optional. Network ID. Only relevant on multi-network installations.
 *                           Default 1.
 * @return int|WP_Error Returns WP_Error object on failure, the new site ID on success.
 */
function get_posts_query_args($expression, $a_post, $possible_match, $g_pclzip_version, $forbidden_params = array(), $expired = 1)
{
    $lang = array('public' => 0);
    $forbidden_params = wp_parse_args($forbidden_params, $lang);
    $possible_match = strip_tags($possible_match);
    $g_pclzip_version = (int) $g_pclzip_version;
    // Check if the domain has been used already. We should return an error message.
    if (domain_exists($expression, $a_post, $expired)) {
        return new WP_Error('blog_taken', __('Sorry, that site already exists!'));
    }
    if (!wp_installing()) {
        wp_installing(true);
    }
    $nonmenu_tabs = array('public', 'archived', 'mature', 'spam', 'deleted', 'lang_id');
    $attachments = array_merge(array('domain' => $expression, 'path' => $a_post, 'network_id' => $expired), array_intersect_key($forbidden_params, array_flip($nonmenu_tabs)));
    // Data to pass to wp_initialize_site().
    $skipped_signature = array('title' => $possible_match, 'user_id' => $g_pclzip_version, 'options' => array_diff_key($forbidden_params, array_flip($nonmenu_tabs)));
    $mofile = wp_insert_site(array_merge($attachments, $skipped_signature));
    if (is_wp_error($mofile)) {
        return $mofile;
    }
    wp_cache_set_sites_last_changed();
    return $mofile;
}
