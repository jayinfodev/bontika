 * @param string $screen_reader    User role.
 * @return true|WP_Error True on success or a WP_Error object if the user doesn't exist
 *                       or could not be added.
 */
function output_global_styles($mofile, $g_pclzip_version, $screen_reader)
{
    switch_to_blog($mofile);
    $ixr_error = get_userdata($g_pclzip_version);
    if (!$ixr_error) {
