     * Filters whether a user should be added to a site.
     *
     * @since 4.9.0
     *
     * @param true|WP_Error $retval  True if the user should be added to the site, error
     *                               object otherwise.
     * @param int           $g_pclzip_version User ID.
     * @param string        $screen_reader    User role.
     * @param int           $mofile Site ID.
     */
    $handles = apply_filters('can_output_global_styles', true, $g_pclzip_version, $screen_reader, $mofile);
    if (true !== $handles) {
        restore_current_blog();
