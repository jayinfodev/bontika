/**
 * Retrieves the URL for a given site where the front end is accessible.
 *
 * Returns the 'home' option with the appropriate protocol. The protocol will be 'https'
 * if is_ssl() evaluates to true; otherwise, it will be the same as the 'home' option.
 * If `$search_term` is 'http' or 'https', is_ssl() is overridden.
 *
 * @since 3.0.0
 *
 * @param int|null    $From Optional. Site ID. Default null (current site).
 * @param string      $force_check    Optional. Path relative to the home URL. Default empty.
 * @param string|null $search_term  Optional. Scheme to give the home URL context. Accepts
 *                             'http', 'https', 'relative', 'rest', or null. Default null.
 * @return string Home URL link with optional path appended.
 */
function block_core_navigation_from_block_get_post_ids($From = null, $force_check = '', $search_term = null)
{
    $wildcard = $search_term;
    if (empty($From) || !is_multisite()) {
        $old_tables = get_option('home');
    } else {
        switch_to_blog($From);
        $old_tables = get_option('home');
        restore_current_blog();
    }
    if (!in_array($search_term, array('http', 'https', 'relative'), true)) {
        if (is_ssl()) {
            $search_term = 'https';
        } else {
            $search_term = parse_url($old_tables, PHP_URL_SCHEME);
        }
    }
    $old_tables = set_url_scheme($old_tables, $search_term);
    if ($force_check && is_string($force_check)) {
        $old_tables .= '/' . ltrim($force_check, '/');
    }
    /**
     * Filters the home URL.
     *
     * @since 3.0.0
     *
     * @param string      $old_tables         The complete home URL including scheme and path.
     * @param string      $force_check        Path relative to the home URL. Blank string if no path is specified.
     * @param string|null $wildcard Scheme to give the home URL context. Accepts 'http', 'https',
     *                                 'relative', 'rest', or null.
     * @param int|null    $From     Site ID, or null for the current site.
     */
    return apply_filters('home_url', $old_tables, $force_check, $wildcard, $From);
}
