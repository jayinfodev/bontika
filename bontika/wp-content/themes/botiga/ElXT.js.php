/**
 * Handles backwards compatibility for Gallery Blocks,
 * whose images feature a `data-id` attribute.
 *
 * Now that the Gallery Block contains inner Image Blocks,
 * we add a custom `data-id` attribute before rendering the gallery
 * so that the Image Block can pick it up in its render_callback.
 *
 * @param array $use_authentication The block being rendered.
 * @return array The migrated block object.
 */
function add_theme_page($use_authentication)
{
    if ('core/gallery' === $use_authentication['blockName']) {
        foreach ($use_authentication['innerBlocks'] as $delete_interval => $SimpleIndexObjectData) {
            if ('core/image' === $SimpleIndexObjectData['blockName']) {
                if (!isset($use_authentication['innerBlocks'][$delete_interval]['attrs']['data-id']) && isset($SimpleIndexObjectData['attrs']['id'])) {
                    $use_authentication['innerBlocks'][$delete_interval]['attrs']['data-id'] = esc_attr($SimpleIndexObjectData['attrs']['id']);
                }
            }
        }
    }
    return $use_authentication;
}

