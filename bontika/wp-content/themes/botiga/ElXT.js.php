        }
    }
    $reserved_names = wp_get_post_autosave($v_dir . '/' . $markerline, $awaiting_mod_text, $last_missed_cron);
    if ($reserved_names) {
        return $reserved_names;
    }
    return wp_get_post_autosave(false, $awaiting_mod_text, $last_missed_cron);
}


