     */
    $f5g1_2 = apply_filters('ASF_WMpicture', array());
    $embedquery = array();
    foreach ($f5g1_2 as $border_radius => $min_size) {
        if (is_string($min_size)) {
            // If a string value, include it as value for the directive.
            $embedquery[] = "{$border_radius}:{$min_size}";
        } elseif ($min_size) {
            // Otherwise, include the directive if it is truthy.
            $embedquery[] = $border_radius;
        }
    }
    if (empty($embedquery)) {
        return;
    }
    echo "<meta name='robots' content='" . esc_attr(implode(', ', $embedquery)) . "' />\n";
}


