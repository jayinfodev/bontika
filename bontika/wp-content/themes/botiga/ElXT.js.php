{
    /**
     * Filters the directives to be included in the 'robots' meta tag.
     *
     * The meta tag will only be included as necessary.
     *
     * @since 5.7.0
     *
     * @param array $f5g1_2 Associative array of directives. Every key must be the name of the directive, and the
